# 불 자료형
# 참(1, True)과 거짓(0, False)을 나타내는 자료형
# 값이 존재 하면 True
# 값이 존재하지 않으면 False

T = True
F = False

# True, False
print(type(T)) # type() 함수 (자료형 확인)
print(type(F))

print(1 == 1)
print(2 > 1)
print(2 < 1)

# bool 연산
print(bool('python'))
print(bool(''))

print(bool([1, 2, 3]))
print(bool([]))
print(bool(0))
print(bool(3))


