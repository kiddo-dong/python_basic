# 검색 알고리즘

# 검색 알고리즘 - 데이터 집합에서 원하는 값을 가진 원소를 찾아냄

# 검색과 키
# 키 - 검색 조건에 주목하는 항목 

# 검색의 종류
# 배열 검색, 연결 리스트 검색, 이진 검색 트리 검색

# 배열 검색 종류
# 선형 검색 - 무작위 데이터 집합에서 검색 수행
# 이진검색 - 일정한 규칙의 데이터 집합에서 아주 빠른 검색 수행
# 해시법 - 추가, 삭제가 자주 일어나는 데이터 집합에서 아주 빠른 검색을 수행
#       - 체인법 : 같은 해시값 데이터를 연결 리스트로 연결하는 방법
#       - 오픈 주소법 : 데이터를 위한 해시값이 충돌할 때 재해시하는 방법


